'''
Find and print the largest and smallest number in a list
Given: data = [8, 2, 15, 1, 9]
'''
print()

data = [8, 2, 15, 1, 9]

max = max(data)
min = min(data)

print("Largest number in the list:", max)
print("Smallest number in the list:", min)